package com.tecsun.sisp.adapter.modules.terminal.entity.response;
/**
 *	诊疗项目
 */
public class MedicalTreatmentProjectVO {
	
	private String projectNo;		// 项目编码
	
	private String projectName;		// 项目名称
	
	private String chargeCategory;	// 收费类别
	
	private String chargeLevel;		// 收费等级
	
	private String paymentCategory;	// 支付类别
	
	private String pinYinCode;		// 拼音码
	
	private String maximumPrice;	// 最高限价
	
	private String unit;			// 单位

	/**
	 * @return the projectNo
	 */
	public String getProjectNo() {
		return projectNo;
	}

	/**
	 * @param projectNo the projectNo to set
	 */
	public void setProjectNo(String projectNo) {
		this.projectNo = projectNo;
	}

	/**
	 * @return the projectName
	 */
	public String getProjectName() {
		return projectName;
	}

	/**
	 * @param projectName the projectName to set
	 */
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}

	/**
	 * @return the chargeCategory
	 */
	public String getChargeCategory() {
		return chargeCategory;
	}

	/**
	 * @param chargeCategory the chargeCategory to set
	 */
	public void setChargeCategory(String chargeCategory) {
		this.chargeCategory = chargeCategory;
	}

	/**
	 * @return the chargeLevel
	 */
	public String getChargeLevel() {
		return chargeLevel;
	}

	/**
	 * @param chargeLevel the chargeLevel to set
	 */
	public void setChargeLevel(String chargeLevel) {
		this.chargeLevel = chargeLevel;
	}

	/**
	 * @return the paymentCategory
	 */
	public String getPaymentCategory() {
		return paymentCategory;
	}

	/**
	 * @param paymentCategory the paymentCategory to set
	 */
	public void setPaymentCategory(String paymentCategory) {
		this.paymentCategory = paymentCategory;
	}

	/**
	 * @return the pinYinCode
	 */
	public String getPinYinCode() {
		return pinYinCode;
	}

	/**
	 * @param pinYinCode the pinYinCode to set
	 */
	public void setPinYinCode(String pinYinCode) {
		this.pinYinCode = pinYinCode;
	}

	/**
	 * @return the maximumPrice
	 */
	public String getMaximumPrice() {
		return maximumPrice;
	}

	/**
	 * @param maximumPrice the maximumPrice to set
	 */
	public void setMaximumPrice(String maximumPrice) {
		this.maximumPrice = maximumPrice;
	}

	/**
	 * @return the unit
	 */
	public String getUnit() {
		return unit;
	}

	/**
	 * @param unit the unit to set
	 */
	public void setUnit(String unit) {
		this.unit = unit;
	}
	
	

}
