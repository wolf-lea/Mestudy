package com.tecsun.sisp.adapter.modules.terminal.entity.response;
/**
 *	定点医疗机构
 */
public class LocalMedicalInstitutionVO {
	
	private String coordinationAreaNo;	// 统筹区号
	
	private String institutionNo;		// 机构编号
	
	private String institutionName;		// 机构名称 
	
	private String institutionLevel;	// 机构等级
	
	private String institutionType;		// 机构类型
	
	private String contactPhone;		// 联系电话
	
	private String address;				// 通讯地址

	/**
	 * @return the coordinationAreaNo
	 */
	public String getCoordinationAreaNo() {
		return coordinationAreaNo;
	}

	/**
	 * @param coordinationAreaNo the coordinationAreaNo to set
	 */
	public void setCoordinationAreaNo(String coordinationAreaNo) {
		this.coordinationAreaNo = coordinationAreaNo;
	}

	/**
	 * @return the institutionNo
	 */
	public String getInstitutionNo() {
		return institutionNo;
	}

	/**
	 * @param institutionNo the institutionNo to set
	 */
	public void setInstitutionNo(String institutionNo) {
		this.institutionNo = institutionNo;
	}

	/**
	 * @return the institutionName
	 */
	public String getInstitutionName() {
		return institutionName;
	}

	/**
	 * @param institutionName the institutionName to set
	 */
	public void setInstitutionName(String institutionName) {
		this.institutionName = institutionName;
	}

	/**
	 * @return the institutionLevel
	 */
	public String getInstitutionLevel() {
		return institutionLevel;
	}

	/**
	 * @param institutionLevel the institutionLevel to set
	 */
	public void setInstitutionLevel(String institutionLevel) {
		this.institutionLevel = institutionLevel;
	}

	/**
	 * @return the institutionType
	 */
	public String getInstitutionType() {
		return institutionType;
	}

	/**
	 * @param institutionType the institutionType to set
	 */
	public void setInstitutionType(String institutionType) {
		this.institutionType = institutionType;
	}

	/**
	 * @return the contactPhone
	 */
	public String getContactPhone() {
		return contactPhone;
	}

	/**
	 * @param contactPhone the contactPhone to set
	 */
	public void setContactPhone(String contactPhone) {
		this.contactPhone = contactPhone;
	}

	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}

	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}
	
	

}
